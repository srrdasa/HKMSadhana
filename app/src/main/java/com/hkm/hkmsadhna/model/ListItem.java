public class ListItem {

    private String id;
    private String maPoints;
    private String jpPoints;
    private String daPoints;
    private String sbPoints;
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMaPoints() {
        return maPoints;
    }

    public void setMaPoints(String maPoints) {
        this.maPoints = maPoints;
    }

    public String getJpPoints() {
        return jpPoints;
    }

    public void setJpPoints(String jpPoints) {
        this.jpPoints = jpPoints;
    }

    public String getDaPoints() {
        return daPoints;
    }

    public void setDaPoints(String daPoints) {
        this.daPoints = daPoints;
    }

    public String getSbPoints() {
        return sbPoints;
    }

    public void setSbPoints(String sbPoints) {
        this.sbPoints = sbPoints;
    }

}
